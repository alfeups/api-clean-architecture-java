package br.com.mibands;

public class Banda {

    private String nome;
    private String genero;

    public Banda(String nome, String genero) {
        this.nome = nome;
        this.genero = genero;

    }

        public String getNome() {
            return nome;
        }

        public String getGenero() {
            return genero;
        }
    }


